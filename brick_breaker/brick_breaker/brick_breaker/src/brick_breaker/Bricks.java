package brick_breaker;


public class Bricks {

	int length=0;
	int width=0;
	int scoreval=0;
	
	void setLength(int length) {
		this.length= length;
	}
	void setWidth(int width) {
		this.width= width;
	}
	int getLength() {
		return length;
	}
	int getWidth() {
		return width;
	}
	void setScore(int score) {
		scoreval= score;
	}
	int getScore() {
		return scoreval;
	}
}
